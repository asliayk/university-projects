public class Product {
    int number;  // catalog number of product
    int quantity; // quantity of product
    int cost; // cost of product
    int total; // total cost of product
    
    // constructor for Product object
    Product(int number, int quantity, int cost, int total) {
    	this.number = number;
    	this.cost = cost;
    	this.quantity = quantity;
    	this.total = total;
    }
	
	
}
